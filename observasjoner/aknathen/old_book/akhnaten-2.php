                <h2>akhnaten – rise and fall</h2>
                <p>The theme was presented me with full weight some eight or nine years ago by one of the most ambitious
                    books from Verlag Könemann – on ‘Egypt and the Pharaonic World’. Studying the list of pharaos I got
                    aware that some minor uncertainty was still prevailing concerning dates, even concerning the 18th
                    dynasty.
                    The numbers given caught my interest, though, and a sequence of questions and ideas surfaced.</p>
                <p>Myself I’m an architect,
                    researcher and author – but neither archae-ologist nor egyptiologist. My special studies concerning
                    the
                    Great Pyramid started in 1977,
                    step by step leading to some document-ations and, in 2004, to a quite extensive twin-exhibition in
                    the
                    Bibliotheca Alexandrina: ‘The House of Memory’
                    (symbols & codes) and ‘Unfolding the World’ (Mercator map discoveries).</p>
                <p>My presentation may have seemed too controversial, both concerning ideas, comments
                    and a few non-Egyptian themes. I admit I haven’t followed well trodden tracks, yet my discoveries
                    into
                    ‘sacred geo-metry’, as well as architectural,
                    geographic and religious topics have been of some value ... not least by indicating that phenomena
                    that
                    are mostly considered separate, do prove striking
                    interconnections.</p>
                <p>What first and foremost caught my interest was the life and fate of Amenhotep IV – the pharaoh who
                    openly
                    crossed the traditional,
                    priestly views and doctrines, announced a monotheistic program, started the building of a new
                    capital
                    city, and changed his name to Akhnaten. His ideals also were
                    expressed in diverse other ways, and his attitudes against traditional Egyptian orthodoxy were
                    clear.</p>
            </div>

            <div class="rightArticle">
                <p>The localization of the new capital, Akhet-Aton, seems an act in order to express a stronger
                    connection
                    between Upper and Lower Egypt – and, most likely, even to get
                    Thebes as well as the Delta centres at arm’s length. The orthodox men in power clearly felt their
                    positions threatened, and this fact would quite naturally nourish potential
                    plans of uproar. Even many of his own officers may have hated him.</p>
                <p>A central theme here is his announcement of the One God. Even his father, Amenhotep III, had
                    presented
                    himself as representative of the Sun, also starting to erect a capital of his own south of Memphis.
                    What
                    seemed so amazing to me was this immensely radical change, and I
                    wondered what could have contributed to it. A deep-going religious vision seemed the only key to
                    understand the phenomenon.</p>
                <p>Akhnaten may have been a visionary, sensitive
                    and gifted ruler – although he must have misjudged the (more or less obvious) conse-quences. When he
                    was
                    forced to surrender his despair naturally was bottomless.
                    But such a fate was a rather common experience to men of power challenging their context, including
                    f.x.
                    the Danish Harold Bluetooth and the Norse Olav Haraldsson.</p>
                <p>The two Nordic pioneers were among the very first Christian kings. Their policy came to be a threat
                    to
                    ruling conventions, causing also their death. At Akhet-Aton we may
                    imagine a hate of yet greater di-mensions, causing Akhnaton’s brutal death – or disappearance. No
                    trace
                    of him seemed to inform about his fate ... until recently,
                    when new finds in the Valley of Kings have decovered a possible hit.</p>
                <p>Whatever can be definitely proven concerning the end of his life, the enigma of the radical king has
                    survived him, giving Egyptian history a just as radical new headline. After sixteen or seventeen
                    years
                    of rule he was virually razed out of his time, and many sculptures,
                    re-liefs and inscriptions praising his magnificence were destroyed. His mother Tiye and his daughter
                    Meritaten were possibly murdered.</p>
