                desert of Midian, near what came to be the ‘Mount of God’. On the Sinai peninsula. There he was
                accepted as shepherd, and for many years to come he endured a life in poverty. But one day he helped
                the local priest’s daughter to water her sheep. He was invited to her home, and eventually married
                her.</p>
